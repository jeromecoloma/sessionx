use anyhow::{anyhow, Result};
use std::collections::BTreeMap;

/// A preset theme. Fields mirror StatusSpec; None means "don't impose a default".
pub struct Theme {
    pub style: BTreeMap<String, String>,
    pub left: Option<String>,
    pub right: Option<String>,
    pub window_format: Option<String>,
    pub current_window_format: Option<String>,
    pub status_interval: Option<u32>,
    /// Format for the per-pane top strip when `status.position: both`.
    /// `None` means the theme has no top strip — `position: both` falls back
    /// to bottom-only with a warning.
    pub pane_border_top: Option<String>,
    /// Helper scripts the theme expects to be materialized into ~/.sessionx/segments/.
    pub builtin_segments: Vec<(&'static str, String)>,
}

/// Resolved format strings with id-style placeholders substituted.
pub struct Resolved {
    pub left: Option<String>,
    pub right: Option<String>,
    pub window_format: Option<String>,
    pub current_window_format: Option<String>,
    pub pane_border_top: Option<String>,
}

impl Theme {
    pub fn resolve(&self, win_style: &str, pane_style: &str) -> Resolved {
        let sub = |s: &Option<String>| -> Option<String> {
            s.as_ref().map(|s| {
                s.replace("__WIN_STYLE__", win_style)
                    .replace("__PANE_STYLE__", pane_style)
            })
        };
        Resolved {
            left: sub(&self.left),
            right: sub(&self.right),
            window_format: sub(&self.window_format),
            current_window_format: sub(&self.current_window_format),
            pane_border_top: sub(&self.pane_border_top),
        }
    }
}

pub const ID_STYLES: &[&str] = &[
    "fsquare", "hsquare", "dsquare", "super", "sub", "roman", "digital", "none", "hide",
];

pub fn validate_id_style(name: &str) -> Result<()> {
    if ID_STYLES.contains(&name) {
        Ok(())
    } else {
        Err(anyhow!(
            "unknown id style '{name}'. Available: {}",
            ID_STYLES.join(", ")
        ))
    }
}

pub fn list() -> &'static [&'static str] {
    &[
        "tokyo-night",
        "catppuccin",
        "dracula",
        "gruvbox",
        "nord",
        "rose-pine",
        "minimal",
    ]
}

pub fn load(name: &str) -> Result<Theme> {
    match name {
        "tokyo-night" => Ok(tokyo_night()),
        "catppuccin" => Ok(catppuccin()),
        "dracula" => Ok(dracula()),
        "gruvbox" => Ok(gruvbox()),
        "nord" => Ok(nord()),
        "rose-pine" => Ok(rose_pine()),
        "minimal" => Ok(minimal()),
        other => Err(anyhow!(
            "unknown theme '{other}'. Available: {}",
            list().join(", ")
        )),
    }
}

fn s(map: &mut BTreeMap<String, String>, k: &str, v: &str) {
    map.insert(k.to_string(), v.to_string());
}

