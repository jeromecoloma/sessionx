const GIT_STATUS_SH: &str = r#"#!/usr/bin/env bash
# auto-generated by sessionx — do not edit
# Prints "<dirty-or-clean-dot> <branch-icon>" for the given path; empty if not a repo.
# Main-line branches (configurable via git_main_branches in ~/.config/sessionx/config.yaml)
# get a git-logo icon; everything else gets a branch icon.
set +e
cd "${1:-.}" 2>/dev/null || exit 0
BRANCH=$(git rev-parse --abbrev-ref HEAD 2>/dev/null)
[ -z "$BRANCH" ] && exit 0
if [ -n "$(git status --porcelain 2>/dev/null)" ]; then
  DOT="●"
else
  DOT="○"
fi
case "$BRANCH" in
  __MAIN_BRANCHES__) ICON="" ;;
  *)                 ICON="" ;;
esac
echo -n " $DOT $ICON"
"#;

fn render_git_status_sh() -> String {
    let branches = crate::agent::global_git_main_branches();
    // Filter to safe shell-glob-friendly chars; warn and drop the rest.
    let safe: Vec<String> = branches
        .into_iter()
        .filter(|b| {
            let ok = !b.is_empty()
                && b.chars()
                    .all(|c| c.is_ascii_alphanumeric() || matches!(c, '.' | '_' | '/' | '-'));
            if !ok {
                eprintln!(
                    "sessionx: ignoring unsafe entry in git_main_branches: '{b}'"
                );
            }
            ok
        })
        .collect();
    let pattern = if safe.is_empty() {
        "main|master".to_string()
    } else {
        safe.join("|")
    };
    GIT_STATUS_SH.replace("__MAIN_BRANCHES__", &pattern)
}

fn shared_segments() -> Vec<(&'static str, String)> {
    vec![
        ("sx-fmt-num", FMT_NUM_SH.to_string()),
        ("sx-os-icon", OS_ICON_SH.to_string()),
        ("sx-git-status", render_git_status_sh()),
    ]
}

struct Palette {
    bg: &'static str,
    fg: &'static str,
    /// Recedes into the bar; used for inactive-window glyphs and right-side block bg.
    dim: &'static str,
    /// Mid-tone for inactive window names — readable but clearly not focused.
    muted: &'static str,
    accent: &'static str,
    accent2: &'static str,
    badge: &'static str,
    /// Background for the session-name block. Must be distinct from `dim`
    /// (the active-window pill bg) so the two blocks don't visually merge.
    session_bg: &'static str,
    icon: &'static str,
    git: bool,
}

fn build_left(p: &Palette) -> String {
    format!(
        "#[fg={bg},bg={accent},bold] {icon} #(~/.sessionx/segments/sx-os-icon.sh)\
         #[fg={fg},bg={badge},nobold] #h \
         #[fg={fg},bg={session_bg},bold] #{{?client_prefix,󰠠 ,#[dim]󰤂 }}#[bold,nodim]#S \
         #[fg={session_bg},bg={bg},nobold]{pill_right}",
        bg = p.bg,
        accent = p.accent,
        icon = p.icon,
        fg = p.fg,
        badge = p.badge,
        session_bg = p.session_bg,
        pill_right = PILL_RIGHT,
    )
}

// Active vs inactive contrast strategy:
//   inactive — flat against bar `bg`, name in `muted`, glyphs in `dim`
//   active   — raised pill on `dim` bg, accent icon + bright bold name,
//              bookended by triangle separators that paint the pill edges
//              cleanly against the bar background.
const PILL_LEFT: &str = ""; //
const PILL_RIGHT: &str = ""; //

fn build_window_format(p: &Palette) -> String {
    format!(
        "#[fg={dim},bg={bg}]#{{?#{{==:#{{pane_current_command}},ssh}},󰣀 , }}\
         #(~/.sessionx/segments/sx-fmt-num.sh #I __WIN_STYLE__)\
         #[fg={muted}]#W\
         #[fg={dim}] #(~/.sessionx/segments/sx-fmt-num.sh #{{window_panes}} __PANE_STYLE__)\
         #[fg={dim}]#{{?window_last_flag,󰁯 , }}",
        dim = p.dim,
        bg = p.bg,
        muted = p.muted
    )
}

fn build_current_window_format(p: &Palette) -> String {
    format!(
        "#[fg={dim},bg={bg}]{pill_left}\
         #[fg={accent},bg={dim},bold]#{{?#{{==:#{{pane_current_command}},ssh}},󰣀 , }}\
         #(~/.sessionx/segments/sx-fmt-num.sh #I __WIN_STYLE__)\
         #[fg={fg},bg={dim},bold]#W\
         #[fg={accent2},bg={dim},nobold] #(~/.sessionx/segments/sx-fmt-num.sh #{{window_panes}} __PANE_STYLE__)\
         #[fg={accent2},bg={dim}]#{{?window_last_flag,󰁯 , }}\
         #[fg={dim},bg={bg}]{pill_right}",
        dim = p.dim,
        bg = p.bg,
        accent = p.accent,
        accent2 = p.accent2,
        fg = p.fg,
        pill_left = PILL_LEFT,
        pill_right = PILL_RIGHT,
    )
}

fn build_right(p: &Palette) -> String {
    // Git segment lives in the per-pane top strip (build_pane_border_top)
    // when status.position is `both`. Bottom bar keeps just date/time so
    // it's not duplicated.
    format!(
        "#[fg={fg},bg={dim}] %m-%d-%Y \
         #[fg={accent2}]• \
         #[fg={fg}]%I:%M %p ",
        dim = p.dim,
        fg = p.fg,
        accent2 = p.accent2,
    )
}

